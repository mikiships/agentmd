def test_markdown_report_is_stable_for_repeated_rendering() -> None:
    report = DriftReport(root_path="/tmp/demo", has_drift=True, files=[_make_drifted_file()])
    first = render_markdown_report(report)
    second = render_markdown_report(report)
    assert first == second
    assert re.search(r"\| Section \| Status \| Detail \|", first)
