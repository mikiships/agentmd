def test_markdown_report_single_file_summary_mentions_filename() -> None:
    report = DriftReport(root_path="/tmp/demo", has_drift=True, files=[_make_drifted_file()])
    output = render_markdown_report(report)
    assert "⚠️ 3 sections drifted in CLAUDE.md" in output


def test_markdown_report_multiple_file_summary_mentions_file_count() -> None:
    file_one = _make_drifted_file()
    file_two = FileDriftReport(
        agent="codex",
        file="AGENTS.md",
        status="stale",
        has_drift=True,
        sections_added=[],
        sections_removed=[],
        sections_changed=["Commands"],
        sections_fresh=[],
        sections_stale=["Commands"],
        stale_details=[
            SectionStaleDetail(
                section="Commands",
                status="changed",
                existing_line_count=1,
                generated_line_count=1,
                diff="--- Commands (existing)\n+++ Commands (generated)\n-old\n+new\n",
            )
        ],
        diff="full diff",
    )
    report = DriftReport(root_path="/tmp/demo", has_drift=True, files=[file_one, file_two])
    output = render_markdown_report(report)
    assert "⚠️ 4 sections drifted across 2 files" in output


@pytest.mark.parametrize(
    ("needle", "status"),
    [
        ("CLAUDE.md :: Project Overview", "stale"),
        ("CLAUDE.md :: Old Section", "missing"),
        ("CLAUDE.md :: New Section", "new"),
        ("CLAUDE.md :: Commands", "fresh"),
    ],
)
def test_markdown_report_table_contains_status_rows(needle: str, status: str) -> None:
    report = DriftReport(root_path="/tmp/demo", has_drift=True, files=[_make_drifted_file()])
    output = render_markdown_report(report)
    assert f"| {needle} | {status} |" in output


def test_markdown_report_escapes_table_pipe_characters() -> None:
    file_report = FileDriftReport(
        agent="claude",
        file="CLAUDE.md",
        status="fresh",
        has_drift=False,
        sections_fresh=["Commands | Scripts"],
    )
    output = render_markdown_report(DriftReport(root_path="/tmp/demo", has_drift=False, files=[file_report]))
    assert "Commands \\| Scripts" in output


@pytest.mark.parametrize(
    ("section", "status_label"),
    [
        ("New Section", "new"),
        ("Project Overview", "stale"),
        ("Old Section", "missing"),
    ],
)
def test_markdown_report_details_map_status_labels(section: str, status_label: str) -> None:
    report = DriftReport(root_path="/tmp/demo", has_drift=True, files=[_make_drifted_file()])
    output = render_markdown_report(report)
    assert f"<summary>CLAUDE.md \u00b7 {section} ({status_label})</summary>" in output


def test_markdown_report_emits_diff_code_fences_for_details() -> None:
    report = DriftReport(root_path="/tmp/demo", has_drift=True, files=[_make_drifted_file()])
    output = render_markdown_report(report)
    assert output.count("```diff") == 3
    assert "--- Project Overview (existing)" in output
    assert "+++ Project Overview (generated)" in output


